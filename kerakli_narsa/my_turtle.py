import turtle as t 

t.bgcolor("black")
t.pensize(5)
t.speed(1)
t.color("red")

t.begin_fill()
t.fillcolor("pink")
t.left(150)
t.forward(180)
t.circle(-90, 180)
t.setheading(60)
t.circle(-90, 180)
t.forward(180)
t.end_fill()

t.hideturtle()

msg = "  I LOVE YOU ‚ù§"
t.penup()
t.goto(-50, -50)
t.pendown()
t.write(msg, align="left", font=("arial", 25, "italic"))

t.done()

